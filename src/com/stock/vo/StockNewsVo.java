package com.stock.vo;


public class StockNewsVo {
	private long id;
	private String title;
	private String author;
	private String strPubDate;
	private String url;
	private String stockContent;
	private String stockFrom;
	private String cacheContent;
	private String strScore;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getStrPubDate() {
		return strPubDate;
	}
	public void setStrPubDate(String strPubDate) {
		this.strPubDate = strPubDate;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getStockContent() {
		return stockContent;
	}
	public void setStockContent(String stockContent) {
		this.stockContent = stockContent;
	}
	public String getStockFrom() {
		return stockFrom;
	}
	public void setStockFrom(String stockFrom) {
		this.stockFrom = stockFrom;
	}
	public String getCacheContent() {
		return cacheContent;
	}
	public void setCacheContent(String cacheContent) {
		this.cacheContent = cacheContent;
	}
	public String getStrScore() {
		return strScore;
	}
	public void setStrScore(String strScore) {
		this.strScore = strScore;
	}
	
	
	
	
}
